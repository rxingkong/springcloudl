@RestController
@RequestMapping("spec")
public class SpecificationController {
    @Autowired
    private SpecificationService specificationService;

    /**
     * 根据cid查询商品规格
     *
     * @param cid
     * @return
     */
    @GetMapping("groups/{cid}")
    public ResponseEntity<List<SpecGroup>> querGroupByCid(@PathVariable("cid") Long cid) {
        return ResponseEntity.ok(specificationService.querGroupByCid(cid));
    }

    /**
     *根据组id查询参数
     * @param gid
     * @return
     */
    @GetMapping("params")
    public ResponseEntity<List<SpecParam>> querParamByGid(@RequestParam("gid") Long gid) {
        return ResponseEntity.ok(specificationService.querParamByGid(gid));
    }
}
